function getExpiracaoCartao(expiracao)
{
	  var dataAtual = new Date();
	  var mesAtual = dataAtual.getMonth()+1;
	  var anoAtual = dataAtual.getFullYear();
	  var mesExpiracao = parseInt((expiracao.toString()).substr(0,2));
	  var anoExpiracao = parseInt((expiracao.toString()).substr(2,2));

	  mesAtual = parseInt(mesAtual.toString());
	  anoAtual = parseInt((anoAtual.toString()).substr(2,2));

	  if((mesExpiracao >=1) && (mesExpiracao <= 12) && (mesExpiracao >= mesAtual) && (anoExpiracao >= anoAtual))
	  {
	    return true;
	  }
	  else
	  {
	    return false;
	  }
}

